public class Printer {
    public static void printer (int value) {
        System.out.println(value);
    }
    public static void printer (boolean value) {
        System.out.println(value);
    }
    public static void printer (double value) {
        System.out.println(value);
    }
    public static void printer (String value) {
        System.out.println(value);
    }
}
